#include <stdio.h>
int main()
{

    int x;
    scanf("%d", &x);
    int hours = x / 3600;
    int pre_miniute = x % 3600;
    int minutes = pre_miniute / 60;
    int seconds = pre_miniute % 60;

    printf("Hours: %d Minutes: %d Seconds: %d", hours, minutes, seconds);

        return 0;
}






































































































































